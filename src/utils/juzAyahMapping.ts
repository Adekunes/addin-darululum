/**
 * Gets the range of ayahs for a given surah in a specific juz
 * @param juzNumber - The juz number
 * @param surahNumber - The surah number
 * @returns Object containing start and end ayah numbers, or null if the surah is not in the juz
 */
export const getAyahRangeForSurahInJuz = (juzNumber: number, surahNumber: number): { startAyah: number; endAyah: number } | null => {
  const juzData = juzAyahMapping[juzNumber];
  if (!juzData) return null;

  const surahData = juzData.find(data => data.surah === surahNumber);
  return surahData ? { startAyah: surahData.startAyah, endAyah: surahData.endAyah } : null;
};

/**
 * Checks if an ayah belongs to a specific juz
 * @param juzNumber - The juz number
 * @param surahNumber - The surah number
 * @param ayahNumber - The ayah number
 * @returns Boolean indicating if the ayah belongs to the juz
 */
export const isAyahInJuz = (juzNumber: number, surahNumber: number, ayahNumber: number): boolean => {
  const range = getAyahRangeForSurahInJuz(juzNumber, surahNumber);
  if (!range) return false;
  return ayahNumber >= range.startAyah && ayahNumber <= range.endAyah;
}; 